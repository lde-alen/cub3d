#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	nb;

	if (nb < 0)
		nb = -n;
	if (n < 0)
		ft_putchar('-');
	if (nb < 10)
		ft_putchar(nb + '0');
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
