#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char *)malloc((size + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while ((int)size >= 0)
	{
		str[(int)size] = '\0';
		size--;
	}
	return (str);
}
